//pangkat

var pangkat = function pangkat(x,y) {
  return Math.pow(x, y);
};

console.log(pangkat(2,3));

//akar kuadrat

var akar = function akar(x) {
  return Math.sqrt(x);
};

console.log(akar(9));

//hipotenusa setiga siku-siku ABC

var hipotenusa = function hipotenusa(a,b) {
  return Math.sqrt(a*a+b*b);
};

console.log(hipotenusa(3,4));

